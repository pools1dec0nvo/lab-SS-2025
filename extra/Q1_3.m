samplerate = 16e3;
% Função geral
y = @(n) 440 * 2.^(n/12);

%Frequências
fB2 = 1/4* y(2);
fC3 = 1/2* y(-9);
fD3 = 1/2* y(-7);
fE3 = 1/2* y(-5);
fG3 = 1/2* y(-2);

%Sequência de sinusoides
x = seqsin(samplerate,...
    0 , 0.7, ...
    fE3 , 0.4, ...
    0 , 0.3, ...
    fE3 , 0.3, ...
    fG3 , 0.4, ...
    fE3 , 0.4, ...
    fD3 , 0.3, ...
    fC3 , 0.8, ...
    0 , 0.2, ...
    fB2 , 0.8, ...
    0 , 0.2, ...
    fE3 , 0.4, ...
    0 , 0.3, ...
    fE3 , 0.3, ...
    fG3 , 0.4, ...
    fE3 , 0.4, ...
    fD3 , 0.3, ...
    fC3 , 0.4, ...
    fD3 , 0.4,  ...
    fC3 , 0.3, ...
    fB2 , 0.3, ...
    0 , 0.7);

soundsc(x, samplerate);